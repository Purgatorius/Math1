public class Main {
    public static void main (String[] args)
    {
        int z1 = 15;
        int z2 = 5;

        System.out.println("15+5 = " +(z1+z2));
        System.out.println("15-5 = " +(z1-z2));
        System.out.println("15*5 = " +(z1*z2));
        System.out.println("15/5 = " +(z1/z2));
        System.out.println("15%5 = " +(z1%z2));
    }
}
